/***********************************************************************
					

程序功能：	本程序为蜂鸣器的基本驱动示例，当按键S2被按下时，蜂鸣器响。
			松开后不响。
**************************************************************************/

#include<reg52.h>

sbit s2=P3^4;
sbit beep=P2^3;

void main()
{
	while(1)
	{
		if(!s2)
			beep=0;
		else
			beep=1;
	}
}