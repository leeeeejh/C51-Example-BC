/********************************************************************
				

程序功能：	让学习板的第一个数码管静态的显示数字3。共阴极数码管

**********************************************************************/
#include <reg51.h>

sbit dula=P2^6;		//段选信号的锁存器控制
sbit wela=P2^7;		//位选信号的锁存器控制

void main()
{
	dula=0;
	wela=0;

	P0=0x4f;	   //数字“3”的代码 01001111,1有效
	dula=1;		   //送数
	dula=0;		   //锁存

	P0=0xfe;	   //选中第一个数码管1111 1110
	wela=1;
	wela=0;

	while(1);
}

