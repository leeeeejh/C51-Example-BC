/*************************************************************************************
程序功能：	用单品机的P1^0管脚模拟脉冲方波输出。
************************************************************************************/

#include <reg52.h>

sbit maichong=P1^0;

void delay600us(void)   //误差 -1.041666666667us
{
    unsigned char a,b;
    for(b=61;b>0;b--)
        for(a=3;a>0;a--);
}


void main()
{
	while(1)
	{
		maichong=1;
		delay600us();
		maichong=0;
		delay600us();		
	}
}
