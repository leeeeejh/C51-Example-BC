int a=45,b=77,c;		//定义实参变量
int max (int x,int y)	//定义函数max，两个形参都为int型
{ 	int z;					//定义返回变量z
	z=(x>y)?x:y;			//利用条件赋值语句将较大者赋给z
	return z;					//返回z
}
void main()					//主函数
{ 	c=max(a,b);			//调用max函数
}
 
 






